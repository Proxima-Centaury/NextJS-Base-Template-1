"use client";
// Components ---------------------------------------------------------------------------------------------------------------------------------------------------------- [ IMPORTS ]
import { ThemeProvider } from "next-themes";
// Dependencies -------------------------------------------------------------------------------------------------------------------------------------------------------- [ IMPORTS ]
import { setCookie } from "cookies-next";
import { themesFallback } from ">_/configuration";
// Hooks --------------------------------------------------------------------------------------------------------------------------------------------------------------- [ IMPORTS ]
import { useEffect } from "react";
// Types --------------------------------------------------------------------------------------------------------------------------------------------------------------- [ IMPORTS ]
import type { ThemeProviderProps } from "next-themes/dist/types";
// Theme ------------------------------------------------------------------------------------------------------------------------------------------------------ [ HELPER COMPONENT ]
const Theme = ({ children, ...props }: ThemeProviderProps): React.JSX.Element => {
    // Properties ---------------------------------------------------------------------------------------------------------------------------------------------------- [ COMPONENT ]
    const { defaultTheme } = props;
    // Effects ------------------------------------------------------------------------------------------------------------------------------------------------------- [ COMPONENT ]
    useEffect(() => setCookie(`theme`, defaultTheme || themesFallback, { expires: new Date(Date.now() + 1000 * 60 * 60 * 24 * 365) , path: `/` }), [ defaultTheme ]);
    // JSX ----------------------------------------------------------------------------------------------------------------------------------------------------------- [ COMPONENT ]
    return <ThemeProvider enableColorScheme { ...props }>
        { children }
    </ThemeProvider>;
};
// Default Export ------------------------------------------------------------------------------------------------------------------------------------------------------ [ EXPORTS ]
export default Theme;